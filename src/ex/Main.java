    private void txSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txSearchActionPerformed
        // TODO add your handling code here:
        txResult.setText("");
        String result = "";
        String codeSearch = txSearch.getText();
        ArrayList<employees> arr = searchByCode(codeSearch);
        if (arr.isEmpty()) {
            result = "Không có kết quả tìm kiếm!";
        } else {
            for (employees e : arr) {
                result += "Code:" + e.getCode() + " |Name:" + e.getName() + " |Salary:" + e.getSalary() + " |Bonus: " + e.getBonus() + "\n";
            }
        }
        txResult.setText(result);
    }//GEN-LAST:event_txSearchActionPerformed

