    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
        // TODO add your handling code here:

        try {
            if (txCode.getText().isEmpty() || txName.getText().isEmpty() || txSalary.getText().isEmpty() || txBonus.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Không được để trống các thành phần!");
                return;
            }
            String code = txCode.getText();
            String name = txName.getText();
            double salary = Double.parseDouble(txSalary.getText());
            double bonus = Double.parseDouble(txBonus.getText());
            if (!checkDuplicate(code)) {
                JOptionPane.showMessageDialog(null, "Code bị trùng.Thử lại!");
                return;
            }
            if (d.addEmployees(code, name, salary, bonus)) {
                JOptionPane.showMessageDialog(null, "Add thành công!");
            } else {
                JOptionPane.showMessageDialog(null, "Add thất bại");
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Sai định dạng số.");
            System.out.println("" + e.getMessage());
            return;
        }
        loadEmployees();
    }//GEN-LAST:event_btAddActionPerformed

