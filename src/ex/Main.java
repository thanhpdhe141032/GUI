    public void loadEmployees() {
        ArrayList<employees> arr = new ArrayList<>();
        arr = d.getAllEmp();
        dtm = (DefaultTableModel) tbList.getModel();
        dtm.setRowCount(0);
        String code;
        String name;
        double salary;
        double bonus;
        for (employees e : arr) {
            code = e.getCode();
            name = e.getName();
            salary = e.getSalary();
            bonus = e.getBonus();
            dtm.insertRow(dtm.getRowCount(), new Object[]{code, name, salary, bonus});
        }
    }

    public void orderEmployees(String order) {
        ArrayList<employees> arr = new ArrayList<>();
        arr = d.getAllOrderEmp(order);
        dtm = (DefaultTableModel) tbList.getModel();
        dtm.setRowCount(0);
        String code;
        String name;
        double salary;
        double bonus;
        for (employees e : arr) {
            code = e.getCode();
            name = e.getName();
            salary = e.getSalary();
            bonus = e.getBonus();
            dtm.insertRow(dtm.getRowCount(), new Object[]{code, name, salary, bonus});
        }
    }

    public ArrayList<employees> searchByCode(String code) {
        ArrayList<employees> arr = new ArrayList<>();
        arr = d.searchByCode(code);
        return arr;
    }

    public boolean checkDuplicate(String code) {
        ArrayList<employees> arr = new ArrayList<>();
        arr = d.getAllEmp();
        for (employees object : arr) {
            if (code.equals(object.getCode())) {
                return false;
            }
        }
        return true;
    }

