package model;

/**
 *
 * @author luat
 */
public class employees {
    private String code;
    private String name;
    private double salary;
    private double bonus; 

    public employees(String code, String name, double salary, double bonus) {
        this.code = code;
        this.name = name;
        this.salary = salary;
        this.bonus = bonus;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getSalary() {
        return salary;
    }

    public void setSalary(double salary) {
        this.salary = salary;
    }

    public double getBonus() {
        return bonus;
    }

    public void setBonus(double bonus) {
        this.bonus = bonus;
    }
    
    
    
}
